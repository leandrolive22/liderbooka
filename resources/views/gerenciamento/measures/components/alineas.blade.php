						<b>A - Ato de improbidade: </b>
						<br>
						- A alínea mais grave, para atos de “desonestidade”, deve-se avaliar bem o caso para utilizar esta alínea.
					</p>
					<p class="text-left">
						<b>B - Incontinência de conduta ou mau procedimento e Inobservância do Head Set :</b>
						<br>
						- Para atos de não cumprimento de normas; 
						<br>
						- Derrubar/não atender/segurar ligações; 
						<br>
						- Reclamação procedente;
						<br>
						- Ausência de marcação de ponto (entrada, pausas e saída);
						<br>
						- Esquecimento do material de trabalho (head set)
						<br>
						- Tabulação incorreta
						<br>
						- Acessar sites não permitidos
						<br>
						- Mau atendimento (ironia, utilizar palavras de baixo calão, grosseria, etc...)
					</p>
					<p class="text-left">
						<b>E - Desídia no desempenho das respectivas funções : </b>
						<br>
						- Para faltas, atrasos, estouro de pausas;
						<br>
						- Não atender as ligações;
						<br>
						- Dormir durante a operação;
					</p>
					<p class="text-left">
						<b>H - Ato de indisciplina ou de insubordinação: </b>
						<br>
						- Para atos de desrespeito (colaborador negando-se a cumprir uma ordem, desrespeito ao colega de trabalho);
						<br>
						- Uso de celular durante a operação; 
						<br>
						- Abandono de posto;
						<br>
						- Vestimenta inadequada;
						<br>
						- Alimentos durante a operação.
